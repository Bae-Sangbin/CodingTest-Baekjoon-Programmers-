SELECT INF.*,SCORE
FROM (SELECT  REST_ID, REST_NAME, FOOD_TYPE, FAVORITES, ADDRESS
      FROM REST_INFO
      WHERE ADDRESS LIKE '서울%') INF JOIN (SELECT REST_ID, ROUND(AVG(REVIEW_SCORE),2) AS SCORE
                                         FROM REST_REVIEW
                                         GROUP BY REST_ID) RV ON INF.REST_ID = RV.REST_ID
ORDER BY SCORE DESC, FAVORITES DESC;